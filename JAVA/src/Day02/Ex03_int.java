package Day02;

public class Ex03_int {
	public static void main(String[] args) {
		// 리터럴 : 프로그램에서 직접 표현 값
		// 정수 리터럴 : 10진수(0~9), 8진수(0~7,리터럴 : 0숫자), 16진수(0~F, 리터럴 : 0x숫자)
		
		//10진수
		int var1 =  10;
		System.out.println("var1 : " + var1);
		//8진수
		int var2 =  020; 
		System.out.println("var2 : " + var2);
		//16진수
		int var3 =  0x10;
		System.out.println("var3 : " + var3);
	}
}